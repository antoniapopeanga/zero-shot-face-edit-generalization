def train_combo(cfg, combo_dir, base_unet, vae, text_encoder, tokenizer,
                noise_sched, train_dataset):
    """Train LoRA for one hyper-param config. Returns list of per-epoch losses."""

    combo_dir.mkdir(parents=True, exist_ok=True)

    unet = build_lora_unet(base_unet, cfg)

    accelerator = Accelerator(
        gradient_accumulation_steps=cfg["grad_accum_steps"],
        mixed_precision="no",
    )

    train_loader = DataLoader(
        train_dataset, batch_size=cfg["batch_size"], shuffle=True
    )

    optimizer = torch.optim.AdamW(
        filter(lambda p: p.requires_grad, unet.parameters()),
        lr=cfg["lr"],
        weight_decay=cfg["weight_decay"],
        betas=(0.9, 0.999),
    )

    lr_scheduler = get_scheduler(
        "constant_with_warmup",
        optimizer=optimizer,
        num_warmup_steps=cfg["warmup_steps"],
        num_training_steps=cfg["num_epochs"] * len(train_loader),
    )

    unet, optimizer, train_loader, lr_scheduler = accelerator.prepare(
        unet, optimizer, train_loader, lr_scheduler
    )
    vae.to(accelerator.device)
    text_encoder.to(accelerator.device)

    def encode_text(prompt):
        tokens = tokenizer(
            prompt, padding="max_length",
            max_length=tokenizer.model_max_length,
            truncation=True, return_tensors="pt",
        ).input_ids.to(accelerator.device)
        with torch.no_grad():
            return text_encoder(tokens).last_hidden_state

    losses     = []
    best_loss  = float("inf")
    unet.train()

    epoch_bar = trange(cfg["num_epochs"], desc="  epochs", leave=False)
    for epoch in epoch_bar:
        epoch_loss = 0.0

        for batch in train_loader:
            with accelerator.accumulate(unet):
                orig_px = batch["original_pixel_values"].to(accelerator.device)
                edit_px = batch["edited_pixel_values"].to(accelerator.device)

                orig_lat = vae.encode(orig_px).latent_dist.sample() * vae.config.scaling_factor
                edit_lat = vae.encode(edit_px).latent_dist.sample() * vae.config.scaling_factor

                noise     = torch.randn_like(edit_lat)
                bsz       = edit_lat.shape[0]
                timesteps = torch.randint(
                    0, noise_sched.config.num_train_timesteps,
                    (bsz,), device=accelerator.device,
                ).long()
                noisy_lat = noise_sched.add_noise(edit_lat, noise, timesteps)

                model_input = torch.cat([noisy_lat, orig_lat], dim=1)
                enc_hidden  = encode_text(EDIT_PROMPT)

                noise_pred = unet(model_input, timesteps,
                                  encoder_hidden_states=enc_hidden).sample
                loss = F.mse_loss(noise_pred, noise)

                accelerator.backward(loss)
                accelerator.clip_grad_norm_(unet.parameters(), 1.0)
                optimizer.step()
                lr_scheduler.step()
                optimizer.zero_grad()

                epoch_loss += loss.item()

        avg = epoch_loss / len(train_loader)
        losses.append(avg)
        epoch_bar.set_postfix(loss=f"{avg:.4f}")

        if avg < best_loss:
            best_loss = avg
            unwrapped = accelerator.unwrap_model(unet)
            unwrapped.save_pretrained(combo_dir / "unet_lora_best")

    # Save loss curve
    fig, ax = plt.subplots(figsize=(7, 3))
    ax.plot(losses, linewidth=1.5, color="#e05c3a")
    ax.set_xlabel("Epoch"); ax.set_ylabel("MSE loss")
    label = (f"r={cfg['r']}  α={cfg['lora_alpha']}  "
             f"lr={cfg['lr']:.0e}  ep={cfg['num_epochs']}")
    ax.set_title(f"LoRA loss — {label}", fontsize=10)
    ax.grid(alpha=0.3)
    plt.tight_layout()
    fig.savefig(combo_dir / "loss_curve.png", dpi=100)
    plt.close(fig)

    # Save config
    cfg_save = {k: (v if not isinstance(v, list) else v) for k, v in cfg.items()}
    cfg_save["best_train_loss"] = float(best_loss)
