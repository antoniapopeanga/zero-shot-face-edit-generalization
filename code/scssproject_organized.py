def pixel_drift(img_a, img_b):
    import numpy as np
    a = np.array(img_a.resize((RESOLUTION, RESOLUTION))).astype(float) / 255.0
    b = np.array(img_b.resize((RESOLUTION, RESOLUTION))).astype(float) / 255.0
    return float(np.mean(np.abs(a - b)))

def run_with_injection(image, inject_until, inject_alpha):
    src_latent = encode_image(image)
    ref_latent = encode_image(reference_image)

    pipe.scheduler.set_timesteps(NUM_STEPS, device=DEVICE)
    timesteps = pipe.scheduler.timesteps

    torch.manual_seed(SEED)
    fixed_noise = torch.randn_like(src_latent)

    def get_noisy_latent(latent, t):

        t_cpu = t.cpu().long() if isinstance(t, torch.Tensor) else int(t)

        alpha_bar = pipe.scheduler.alphas_cumprod[t_cpu].to(
            device=DEVICE,
            dtype=latent.dtype
        )

        alpha_bar = alpha_bar.view(1, 1, 1, 1)

        return (
            torch.sqrt(alpha_bar) * latent
            + torch.sqrt(1.0 - alpha_bar) * fixed_noise
        )

    inject_steps = max(1, int(inject_until * NUM_STEPS))

    structural_latents = {}

    for i in range(inject_steps):

        src_noisy = get_noisy_latent(
            src_latent,
            timesteps[i]
        )

        ref_noisy = get_noisy_latent(
            ref_latent,
            timesteps[i]
        )

        # Mostly preserve source identity
        # Small amount of beard information
        mixed = (
            0.90 * src_noisy
            + 0.10 * ref_noisy
        )

        structural_latents[i] = mixed
    def injection_callback(pipe_obj, step_index, timestep, kwargs):

        if step_index >= inject_steps:
            return kwargs

        current = kwargs["latents"]
        struct_lat = structural_latents[step_index]

        if struct_lat.shape != current.shape:
            struct_lat = torch.nn.functional.interpolate(
                struct_lat,
                size=current.shape[2:],
                mode="bilinear",
                align_corners=False,
            )

        progress = step_index / max(inject_steps - 1, 1)

        blend = (
            inject_alpha
            * 0.5
            * (1.0 + math.cos(math.pi * progress))
        )

        kwargs["latents"] = (
            blend * struct_lat.expand_as(current)
            + (1.0 - blend) * current
        )

        return kwargs

    result = pipe(
        prompt=EDIT_PROMPT,
        image=image,
        num_inference_steps=NUM_STEPS,
        guidance_scale=GUIDANCE_SCALE,
        image_guidance_scale=IMAGE_GUIDANCE_SCALE,
        generator=make_generator(),
        callback_on_step_end=injection_callback,
        callback_on_step_end_tensor_inputs=["latents"],
    ).images[0]

    return result

