@torch.no_grad()
def clip_preservation(original: Image.Image, edited: Image.Image) -> float:
    inputs = clip_processor(images=[original, edited], return_tensors="pt").to(device)
    feats  = clip_model.get_image_features(**inputs)
    feats  = feats / feats.norm(dim=-1, keepdim=True)
    return (feats[0] @ feats[1]).item()

