for inject_until, inject_alpha in tqdm(INJECT_GRID, desc="Grid combos"):
    tag     = f"u{inject_until}_a{inject_alpha}"
    out_dir = PILOT_DIR / tag
    out_dir.mkdir(exist_ok=True)

    drift_sum = 0.0
    for image_id in PILOT_IDS:
        orig = Image.open(INPUTS_DIR / f"{image_id}.png").convert("RGB")
        out  = run_with_injection(orig, inject_until, inject_alpha)
