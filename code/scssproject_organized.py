def clip_text_probs(image_path, labels):
    image = Image.open(image_path).convert("RGB")

    inputs = clip_processor(
        text=labels,
        images=image,
        return_tensors="pt",
        padding=True
    ).to(device)

    with torch.no_grad():
        outputs = clip_model(**inputs)
        probs = outputs.logits_per_image.softmax(dim=1)[0].detach().cpu().numpy()

    return dict(zip(labels, probs))

def clip_image_similarity(path_a, path_b):
    image_a = Image.open(path_a).convert("RGB")
    image_b = Image.open(path_b).convert("RGB")

    inputs_a = clip_processor(images=image_a, return_tensors="pt").to(device)
    inputs_b = clip_processor(images=image_b, return_tensors="pt").to(device)

    with torch.no_grad():
        out_a = clip_model.get_image_features(**inputs_a)
        out_b = clip_model.get_image_features(**inputs_b)

        if hasattr(out_a, "image_embeds"):
            emb_a = out_a.image_embeds
            emb_b = out_b.image_embeds
        elif hasattr(out_a, "pooler_output"):
            emb_a = out_a.pooler_output
            emb_b = out_b.pooler_output
        else:
            emb_a = out_a
            emb_b = out_b

        emb_a = emb_a / emb_a.norm(dim=-1, keepdim=True)
        emb_b = emb_b / emb_b.norm(dim=-1, keepdim=True)

        sim = (emb_a @ emb_b.T).item()

    return sim

