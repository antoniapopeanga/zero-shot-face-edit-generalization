def pil_to_np(img):
    return np.array(img.convert("RGB")).astype(np.float32) / 255.0

def np_to_pil(arr):
    arr = np.clip(arr * 255.0, 0, 255).astype(np.uint8)
    return Image.fromarray(arr)

def normalize_pair(orig_img, edit_img):
    orig_np = pil_to_np(orig_img)
    edit_np = pil_to_np(edit_img)

    # ensure contiguous float32 — OpenCV is strict about this
    orig_np = np.ascontiguousarray(orig_np, dtype=np.float32)
    edit_np = np.ascontiguousarray(edit_np, dtype=np.float32)

    orig_ycc = cv2.cvtColor(orig_np, cv2.COLOR_RGB2YCrCb)
    edit_ycc = cv2.cvtColor(edit_np, cv2.COLOR_RGB2YCrCb)

    y_edit = edit_ycc[:,:,0]
    y_orig = orig_ycc[:,:,0]

    y_edit_norm = (y_edit - y_edit.mean()) / (y_edit.std() + 1e-6)
    y_edit_norm = y_edit_norm * y_orig.std() + y_orig.mean()

    edit_ycc[:,:,0] = np.clip(y_edit_norm, 0, 1)
    edit_normalized = cv2.cvtColor(
        np.ascontiguousarray(edit_ycc, dtype=np.float32),  # contiguous before converting back
        cv2.COLOR_YCrCb2RGB
    )
    return np_to_pil(edit_normalized)

class InstructEditDataset(Dataset):
    def __init__(self, df, inputs_dir, outputs_dir, prompt, resolution=256):
        self.records     = df.reset_index(drop=True)
        self.inputs_dir  = Path(inputs_dir)
        self.outputs_dir = Path(outputs_dir)
        self.prompt      = prompt
        self.to_tensor   = transforms.Compose([
            transforms.Resize(resolution, interpolation=transforms.InterpolationMode.LANCZOS),
            transforms.CenterCrop(resolution),
            transforms.ToTensor(),
            transforms.Normalize([0.5], [0.5]),
        ])

    def __len__(self):
        return len(self.records)

    def __getitem__(self, idx):
        row  = self.records.iloc[idx]
        orig = Image.open(self.inputs_dir / f"{row.image_id}.png").convert("RGB")
        edit = Image.open(self.outputs_dir / f"{row.image_id}_facial_hair_best.png").convert("RGB")

        edit = normalize_pair(orig, edit)  # ← only change from your original

        if torch.rand(1) > 0.5:
            brightness = 0.9 + torch.rand(1).item() * 0.2
            contrast   = 0.9 + torch.rand(1).item() * 0.2
            orig = transforms.functional.adjust_brightness(orig, brightness)
            edit = transforms.functional.adjust_brightness(edit, brightness)  # same value
            orig = transforms.functional.adjust_contrast(orig, contrast)
            edit = transforms.functional.adjust_contrast(edit, contrast)      # same value

